void des_generate_keys(uint64_t *output, uint8_t *input)
{
    uint32_t C = 0;
    uint32_t D = 0;
    uint64_t K = 0;
    uint64_t key = 0;
    uint64_t mask = 0;

    ft_bzero(output, 128);
    ft_memcpy(&key, input, 8);

    if (little_endian()) {
        key = byteswap64(key);
    }

    for (int i = 0; i <= 55; i++) {
        mask = 0x1ULL << (64 - KP1[i]);
        K |= ((key & mask) >> (64 - KP1[i])) << (55 - i);
    }

    C = (K >> 28) & 0xFFFFFFF;
    D = K & 0xFFFFFFF;

    for (int i = 0; i < 16; i++) {
        C = (C << key_rotations[i] & 0xFFFFFFF) | C >> (28 - key_rotations[i]);
        D = (D << key_rotations[i] & 0xFFFFFFF) | D >> (28 - key_rotations[i]);
        K = ((uint64_t)C << 28) | D;

        for (int j = 0; j <= 47; j++) {
            mask = 0x1ULL << (56 - KP2[j]);
            output[i] |= ((K & mask) >> (56 - KP2[j])) << (47 - j);
        }
    }
}

uint64_t des_permutation(uint64_t input, int table[], int len, int max_val) 
{
    uint64_t mask = 0;
    uint64_t res = 0;

    for (int j = 0; j < len; j++) {
        mask = 0x1ULL << (max_val - table[j]);
        res |= ((input & mask) >> (max_val - table[j])) << (len - j - 1);
    }
    return (res);
}

uint64_t des_sbox_permutation(uint64_t input)
{
    uint8_t sextet = 0;
    uint8_t row = 0;
    uint8_t column = 0;
    uint64_t res = 0;

    for (int i = 0; i < 8; i++) {
        sextet = input >> (42 - i * 6) & 0x3F;
        row = ((sextet & 0x20) >> 4) | (sextet & 0x1);
        column = sextet >> 1 & 0xF;
        res |= SBOX[i][row][column] << (28 - i * 4);
    }
    return (res);
}

void des_block(uint8_t *dst, uint8_t *src, uint8_t *key,
               uint8_t *iv, int decode)
{
    uint64_t keys[16];
    uint64_t msg = 0;
    uint64_t tmp = 0;
    uint64_t ip = 0;
    uint64_t V = 0;
    uint64_t L = 0;
    uint64_t R = 0;

    ft_memcpy(&msg, src, 8);
    if (little_endian()) {
        msg = byteswap64(msg);
    }
    if (iv) {
        ft_memcpy(&V, iv, 8);
        if (little_endian()) {
            V = byteswap64(V);
        }
        if (!decode) {
            msg ^= V;
        }
    }
    des_generate_keys(keys, key);
    ip = des_permutation(msg, IP, 64, 64);
    L = (ip >> 32);
    R = ip & 0xFFFFFFFF;
    for (int i = 0; i < 16; i++) {
        tmp = R;
        R = des_permutation(R, EP, 48, 32);
        R = des_sbox_permutation(R ^ keys[decode ? 15 - i : i]);
        R = des_permutation(R, PBOX, 32, 32);
        R ^= L;
        L = tmp;
    }
    ip = des_permutation(R << 32 | L, FP, 64, 64);
    if (iv && decode) {
        ip ^= V;
    }
    for (int i = 0; i < 8; i++) {
        dst[i] = ip >> (56 - i * 8) & 0xFF;
    }
}
